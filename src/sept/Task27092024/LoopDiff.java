package sept.Task27092024;

public class LoopDiff {

    public static void main(String[] args) {

        int count = 1;
        while (count <= 5) {      //condition first

            System.out.println("Count is: " + count); //execution later
            count++;      //increment later
        }




        int count2 = 1;
        do {


            System.out.println("Count2 is: " + count2);  //execution first
            count2++; // increment first

        }while (count2 <= 5); //condition later


        }


    }

